// Example 1 - Using printf

#include <stdio.h>

int main() {
    int num = 42;
    float floatNum = 3.14159;
    char ch = 'A';
    char name[] = "Hello, World!";

    printf("Integer: %d\n", num);
    printf("Float: %.2f\n", floatNum);
    printf("Character: %c\n", ch);
    printf("String: %s\n", name);

    return 0;
}

