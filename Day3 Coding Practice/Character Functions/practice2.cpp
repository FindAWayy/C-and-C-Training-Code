// using tolower and toupper 

#include <stdio.h>
#include <ctype.h>

int main() {
    char c = 'A';
    printf("Lowercase of %c is %c\n", c, tolower(c));
    return 0;
}

////

#include <stdio.h>
#include <ctype.h>

int main() {
    char c = 'b';
    printf("Uppercase of %c is %c\n", c, toupper(c));
    return 0;
}



